package payment;
/**
 * class payment to pay for order
 */
public abstract class payment 
{
    protected double price;

//----------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------
    /**
     * default constructor 
     */
    public payment()
    {
        price =0;
    }

//----------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------
  
    /**
     * constructor
     * 
     * @param price is the price of the payment for order
     */
    public payment(double price)
    {
        this.price = price;
    }
//----------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------
    /**
     * getter for the price
     * 
     * @return the price of the payment for order
     */
    public double getPrice() 
    {
        return price;
    }

//----------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------
  
    /**
     * setter for the price
     * 
     * @param price is the price of the payment for order
     */
    public void setPrice(double price) 
    {
        this.price = price;
    }

//----------------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------------
    /**
     * pay for the order
     * 
     * @param userid is the user id 
     */
    public abstract void pay(int userid);

}

